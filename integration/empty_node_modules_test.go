package integration_test

import (
	"os"
	"path/filepath"
	"testing"

	"github.com/paketo-buildpacks/occam"
	"github.com/sclevine/spec"

	. "github.com/onsi/gomega"
)

func testEmptyNodeModules(t *testing.T, context spec.G, it spec.S) {
	var (
		Expect = NewWithT(t).Expect

		pack   occam.Pack
		docker occam.Docker

		name   string
		source string
	)

	it.Before(func() {
		pack = occam.NewPack()
		docker = occam.NewDocker()

		var err error
		name, err = occam.RandomName()
		Expect(err).NotTo(HaveOccurred())
	})

	it.After(func() {
		Expect(docker.Volume.Remove.Execute(occam.CacheVolumeNames(name))).To(Succeed())
		Expect(os.RemoveAll(source)).To(Succeed())
	})

	context("when the node_modules directory is empty", func() {
		it("builds a working OCI image for an app that doesn't have a package-lock.json", func() {
			var err error
			source, err = occam.Source(filepath.Join("testdata", "empty_node_modules"))
			Expect(err).NotTo(HaveOccurred())

			_, logs, err := pack.Build.
				WithPullPolicy("never").
				WithBuildpacks(
					nodeURI,
					buildpackURI,
					buildPlanURI,
				).
				Execute(name, source)
			Expect(err).To(HaveOccurred())

			Expect(logs).To(ContainSubstring("vendored node_modules have unmet dependencies"))
			Expect(logs).To(ContainSubstring("UNMET DEPENDENCY express"))
		})
	})
}
